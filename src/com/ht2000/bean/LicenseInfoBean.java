package com.ht2000.bean;


public class LicenseInfoBean {

	private String desc = null;
	private String endTimeAbs = null;
	private String licenseLevel = null;
	private String maxInputTask = null;
	private String maxOutputTask = null;
	private String maxWorkTimes = null;
	private String published = null;
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getEndTimeAbs() {
		return endTimeAbs;
	}
	public void setEndTimeAbs(String endTimeAbs) {
		this.endTimeAbs = endTimeAbs;
	}
	public String getLicenseLevel() {
		return licenseLevel;
	}
	public void setLicenseLevel(String licenseLevel) {
		this.licenseLevel = licenseLevel;
	}
	public String getMaxInputTask() {
		return maxInputTask;
	}
	public void setMaxInputTask(String maxInputTask) {
		this.maxInputTask = maxInputTask;
	}
	public String getMaxOutputTask() {
		return maxOutputTask;
	}
	public void setMaxOutputTask(String maxOutputTask) {
		this.maxOutputTask = maxOutputTask;
	}
	public String getMaxWorkTimes() {
		return maxWorkTimes;
	}
	public void setMaxWorkTimes(String maxWorkTimes) {
		this.maxWorkTimes = maxWorkTimes;
	}
	public String getPublished() {
		return published;
	}
	public void setPublished(String published) {
		this.published = published;
	}
	
	
}
